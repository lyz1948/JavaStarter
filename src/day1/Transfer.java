package day1;

public class Transfer {
  public static void main(String[] args) {
    int n = 97;
    // to binary
    System.out.println(Integer.toBinaryString(n));
    // to octal
    System.out.println(Integer.toOctalString(n));
    // to hex
    System.out.println(Integer.toHexString(n));
  }
}
