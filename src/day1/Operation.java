package day1;

public class Operation {
  public static void main(String[] args) {
    int x = 10;
    int y = 3;

    System.out.println(x%y);
    System.out.println(x/y);
    System.out.println(x/(double)y);

    System.out.println(++x);
    System.out.println(x);
    System.out.println(x++);
    System.out.println(x);
    System.out.println(--x);
    System.out.println(x--);

    System.out.println(x);
  }
}
